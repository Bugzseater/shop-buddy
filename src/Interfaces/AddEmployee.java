    public void loadTable() {
        try {
            
            ResultSet resultSet = (ResultSet) MYSQL.search("SELECT * FROM employee");
            ResultSet count = MYSQL.search("SELECT COUNT(*) FROM employee");

              DefaultTableModel model = (DefaultTableModel) employeeTable.getModel();
            model.setRowCount(0);

         

      
            while (resultSet.next()) {
                Vector<String> v = new Vector();
                v.add(resultSet.getString("employee_ID"));
                v.add(resultSet.getString("name"));
                v.add(resultSet.getString("address"));
                v.add(resultSet.getString("mobile_no"));
                v.add(resultSet.getString("possition"));
                v.add(resultSet.getString("salary"));

                model.addRow(v);
                employeeTable.setModel(model);
            }
            
             if (count.next()) {
                int cnt = count.getInt(1);
                countbox.setText(String.valueOf(cnt));
            }

        } catch (Exception e) {
        }
    }
    



