    private void employeeTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_employeeTableMouseClicked
        // TODO add your handling code here:
    if (evt.getClickCount() == 2) {
        int selectedRow = employeeTable.getSelectedRow();
        
        if (selectedRow != -1) {  // Ensure a row is selected
            try {
                String employeid = String.valueOf(employeeTable.getValueAt(selectedRow, 0));
                eid.setText(employeid);
                String empname = String.valueOf(employeeTable.getValueAt(selectedRow, 1));
                ename.setText(empname);
                String empadd = String.valueOf(employeeTable.getValueAt(selectedRow, 2));
                eaddress.setText(empadd);
                String empmobile = String.valueOf(employeeTable.getValueAt(selectedRow, 3));
                emobile.setText(empmobile);
                String empsal = String.valueOf(employeeTable.getValueAt(selectedRow, 5));
                esalary.setText(empsal);
                String position = String.valueOf(employeeTable.getValueAt(selectedRow, 4));
                eposs.setSelectedItem(position);

            } catch (Exception e) {
                // Handle potential exceptions
                
            }
        }
    }
    }//GEN-LAST:event_employeeTableMouseClicked

