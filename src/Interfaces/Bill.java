    public void subtotal() {
        int sum = 0;
        for (int i = 0; i < billtable.getRowCount(); i++) {
            // Cast the Object returned by getValueAt to String, then parse it to int
            int value = Integer.parseInt((String) billtable.getValueAt(i, 4));
            sum = sum + value;
        }
        // Convert int sum to String before setting it as text
        subtot.setText(String.valueOf(sum));
    }


