    private void billbtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_billbtn1ActionPerformed
        // TODO add your handling code here:
        String customerid = cusid.getText();
        String totl = subtot.getText();
        String type = typebox.getSelectedItem().toString();
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
        String dat = s.format(d);


        if (customerid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "First Name field is empty", "Warning", JOptionPane.WARNING_MESSAGE);
            cusid.grabFocus();
        } 
        else {
            MYSQL.iud("INSERT INTO bill (customer_id,date,total_price,bill_type) VALUES('" + customerid + "','" + dat + "','" + totl + "','" + type + "') ");
            
            cusid.setText("");
            subtot.setText("");
            cashout.setText("");
            balanceprint.setText("");
            DefaultTableModel tableModel = (DefaultTableModel) billtable.getModel();
            tableModel.setRowCount(0);

        }
        
        try {
            printBill.print();
            clear();
        } catch (PrinterException ex) {
            JOptionPane.showMessageDialog(null, "Cannot print: ");
        }
    }//GEN-LAST:event_billbtn1ActionPerformed

