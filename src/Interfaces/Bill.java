    public void updatedata(){
        String productid = pid.getText();
        int enterqty = Integer.parseInt(qtyn.getText());
        try {
            ResultSet resultSet = MYSQL.search("SELECT * FROM `products` WHERE `product_id` = '"+productid+"'");
            

              // Assuming `tabaleqty` is a ResultSet object
                while (resultSet.next()) { // Move the cursor to the first row
                    // Retrieve the quantity from the ResultSet
                    int currentQty = resultSet.getInt("quntity");
                    int newqnt = currentQty - enterqty ; // Calculate the new quantity
                    MYSQL.iud("UPDATE `products` SET quntity = '"+newqnt+"' WHERE `product_id` = '"+productid+"'");
                }

        } catch (Exception ex) {
            
            ex.printStackTrace();
            
        }
    }
    
